namespace Calculadora
{
    public class Contas()
    {
        public static float Soma(float a, float b)
        {
            return a + b;
        }
        public static float Subtracao(float a, float b)
        {
            return a - b;
        }
        public static float Multiplicacaoo(float a, float b)
        {
            return a * b;
        }
        public static float Divisao(float a, float b)
        {
            return a / b;
        }
        
    }

}